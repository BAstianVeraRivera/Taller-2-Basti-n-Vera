public class Motor {
	private int nroMotor;
	private int cmCubicos;

	public int getNroMotor() {
		return this.nroMotor;
	}

	public void setNroMotor(int nroMotor) {
		this.nroMotor = nroMotor;
	}

	public int getCmCubicos() {
		return this.cmCubicos;
	}

	public void setCmCubicos(int cmCubicos) {
		this.cmCubicos = cmCubicos;
	}
	public Motor (int nroMotor, int cmCubicos){
		this.nroMotor=nroMotor;
		this.cmCubicos=cmCubicos;
	}
}